/*******************************************************************************
  Function:
    void APP_Tasks ( void )

  Summary:
    MPLAB Harmony Demo application tasks function

  Description:
    Make a subroutine call to receive from a queue -- don?t call
    a FreeRTOS routine here directly. Return the
    received sensor value as a variable. Make a subroutine call to an 
    implementation of a four-state state-machine, where the received sensor 
    value is an argument passed to the subroutine.

  Precondition:
    The system and application initialization ("SYS_Initialize") should be
    called before calling this.

  Parameters:
    None.

  Returns:
    None.

  Example:
    <code>
    APP_Tasks();
    </code>

  Remarks:
    This routine must be called from SYS_Tasks() routine.
 */

void APP_Tasks( void );


